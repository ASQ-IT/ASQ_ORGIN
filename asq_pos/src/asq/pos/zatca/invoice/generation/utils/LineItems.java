package asq.pos.zatca.invoice.generation.utils;

public class LineItems {

	private String item_line_no;
	private String productCode;
	private String engDescription;
	public String getItem_line_no() {
		return item_line_no;
	}
	public void setItem_line_no(String item_line_no) {
		this.item_line_no = item_line_no;
	}
	public String getProductCode() {
		return productCode;
	}
	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}
	public String getEngDescription() {
		return engDescription;
	}
	public void setEngDescription(String engDescription) {
		this.engDescription = engDescription;
	}
	
}
